// Plus Minus Expander 1
$(document).ready(function() {
	$(".pnt-toggle1 > div").click(function() {
		$(".pnt-toggle1 > div.itm-toggle1").not($(this).siblings()).slideUp();
		$(this).siblings(".itm-toggle1").slideToggle();
		// toggle open/close symbol
		if ($('.itm1 a').html() == '<span class="x-minus">-</span>') {
			$('.itm1 a').html('<span class="x-plus">+</span>');
		} else {
			$('.itm1 a').html('<span class="x-minus">-</span>');
		}
	});
});


